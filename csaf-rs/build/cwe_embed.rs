use std::{fs, path::Path};

const CWE_ASSET_DIR: &str = "assets/cwe";
const CWE_FILES_RS: &str = "src/cwe_files.rs";

/// Scans [`CWE_ASSET_DIR`] for CSV files and writes a [`CWE_FILES_RS`] source file containing a
/// static array of `(filename, bytes)` pairs embedded via `include_bytes!`.
pub fn generate_cwe_file_lookup() {
    let manifest_dir = std::env::var("CARGO_MANIFEST_DIR").unwrap();
    let cwe_dir = Path::new(&manifest_dir).join(CWE_ASSET_DIR);
    let dest = Path::new(&manifest_dir).join(CWE_FILES_RS);

    // collect and sort CSV filenames so the generated output is deterministic
    let mut files: Vec<String> = fs::read_dir(&cwe_dir)
        .expect(&format!("{CWE_ASSET_DIR} directory should exist"))
        .filter_map(|e| e.ok())
        .filter(|e| e.path().extension().map(|x| x == "csv").unwrap_or(false))
        .map(|e| e.file_name().to_string_lossy().into_owned())
        .collect();
    files.sort();

    // build one `include_bytes!` entry per file.
    let entries = files
        .iter()
        .map(|name| {
            format!(r#"    ("{name}", include_bytes!("../{CWE_ASSET_DIR}/{name}")),"#)
        })
        .collect::<Vec<_>>()
        .join("\n");

    // don't touch header
    let content = format!(
        "// @generated by build/cwe_embed.rs — do not edit by hand.\n\
         pub(crate) static CWE_CSV_FILES: &[(&str, &[u8])] = &[\n\
         {entries}\n\
         ];\n"
    );

    // only write if content changed to avoid unnecessary recompilation
    if fs::read_to_string(&dest).unwrap_or_default() != content {
        fs::write(&dest, &content).expect(&format!("Failed to write {CWE_FILES_RS}"));
    }

    // re-run this build script whenever a CSV file is added, removed, or renamed / moved
    println!("cargo:rerun-if-changed={CWE_ASSET_DIR}");
}